class Lever < Pullable
    def targets 
        ["lever","handle","switch"]
    end
    def load_special_properties
        content.assemble if @@check.include?(self)
    end
	def backdrop													
		print "	   - An iron lever juts out from\n"					
		print "	     the wall where you stand.\n\n"				
	end
	def view
		if @@check.none?(self)									
			print "	   - This lever isn't pulled yet.\n"			
			print "	     It could do anything.\n\n"	
		else print "	   - It's stuck down and locked.\n\n"
		end
	end
end	



class Ore < Portable 
    def targets
        subtype | ["ore","metal","raw ore","raw metal"]
    end
    def view 
        puts "	   - It's a chunk of #{subtype[0]} from"
		puts "	     beneath the goblin dungeons."
        puts "	     It's used to to forge tools"
        puts "	     and weapons.\n\n"
        view_profile
        print "\n"
    end
end

class Silver < Ore      
    def subtype
       ["silver"]
    end
    def initialize
        @profile = {:deflects => "evil", :strength => 3}
    end
end

class Copper < Ore      
    def subtype
       ["copper"]
    end
    def initialize
        @profile = {:strength => 4}
    end
end



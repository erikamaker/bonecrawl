class Jewel < Portable 
    def targets
        subtype | ["gem","jewel","geode","stone","crystal"]
    end
    def view 
        puts "	   - It's a #{subtype[0]}. Jewels like"
		puts "	     this are rarely found before"
        puts "	     Trolls can eat them.\n\n"
        view_profile
        print "\n"
    end
end

class Blue < Jewel      
    def subtype
       ["blue gem"]
    end
    def initialize
        @profile = {:magic => "cold"}
    end
end

class Pink < Jewel      
    def subtype
       ["pink gem"]
    end
    def initialize
        @profile = {:magic => "warm"}
    end
end